// Bitwise left shift, right shift

class BitwiseShift{
public static void main(String args[]){
int x=2;
int res=x<<1;
System.out.println(res);
int y=3;
int res1=y<<2;
System.out.println(res1);
int a=20;
int b=a>>1;
System.out.println(b);
int p=80;
int q=p>>3;
System.out.println(q);
}
}