//All Unary Operators

public class UnaryOperator{
public static void main(String args[]){
int num=76;
System.out.println(num);
num=-num;
System.out.println(num);
int a=20;
a++;
System.out.println(a);
a--;
System.out.println(a);
boolean flag=false;
System.out.println(flag);
System.out.println(!flag);
}
}